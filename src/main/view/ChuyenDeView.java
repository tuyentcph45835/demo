    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int chon,index;
        String ma;
        index = tblBang.getSelectedRow();
        chon = JOptionPane.showConfirmDialog(this, "Ban co muon xoa du lieu", "Xoa du lieu", JOptionPane.YES_NO_CANCEL_OPTION, 3,null);
        if(chon == 0){
            ma = tblBang.getValueAt(index, 0).toString();
            cd.remove(ma);
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
            this.fillTable(cd.getAll());
            
        }else{
            JOptionPane.showMessageDialog(this, "Xoa that bai");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int index = tblBang.getSelectedRow();
        if (index == -1) {
            JOptionPane.showConfirmDialog(this, "Vui long chon 1 dong de sua", "Loi", JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        String ma = tblBang.getValueAt(index, 0).toString();
        ChuyenDe cc = this.readForm();
        int result = cd.update(ma,cc);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            this.fillTable(cd.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int chon;
        chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm dữ liệu", "Thêm dữ liệu", JOptionPane.YES_NO_CANCEL_OPTION, 3, null);
        if (chon == 0) {
            cd.add(this.readForm());
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            this.fillTable(cd.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm không thành công");
        }
    }//GEN-LAST:event_btnThemActionPerformed

