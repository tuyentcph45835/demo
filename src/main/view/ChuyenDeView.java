    void fillTable(List<ChuyenDe> list){
        model = (DefaultTableModel) tblBang.getModel();
        model.setRowCount(0);
        for (ChuyenDe chuyenDe : list) {
            model.addRow(chuyenDe.toDataRow());
        }
    }
    void showData(int index){
        txtMa.setText(tblBang.getValueAt(index, 0).toString());
        txtTen.setText(tblBang.getValueAt(index, 1).toString());
        txtHocPhi.setText(tblBang.getValueAt(index, 2).toString());
        txtThoiLuong.setText(tblBang.getValueAt(index, 3).toString());
        txaMoTa.setText(tblBang.getValueAt(index, 4).toString());
    }
    ChuyenDe readForm() {
        String maCD, tenCD, moTa;
        Long hocPhi;
        int thoiLuong;
        maCD = txtMa.getText().trim();
        tenCD = txtTen.getText().trim();
        hocPhi = Long.parseLong(txtHocPhi.getText().trim());
        thoiLuong = Integer.parseInt(txtThoiLuong.getText().trim());
        moTa = txaMoTa.getText().trim();
        return new ChuyenDe(maCD, tenCD, hocPhi, thoiLuong, moTa);
    }
